export default function TechIcon({ tech, index = 0 }: TechIconProps) {
  const prefersReducedMotion = useReducedMotion()
  const [showTooltip, setShowTooltip] = useState(false)
  const [tooltipPosition, setTooltipPosition] = useState<'top' | 'bottom'>('top')
  const iconRef = useRef<HTMLDivElement>(null)
  const config = techConfigs[tech]

  // Determine tooltip position based on icon position in viewport
  useEffect(() => {
    if (showTooltip && iconRef.current) {
      const rect = iconRef.current.getBoundingClientRect()
      // If icon is in top half of viewport, show tooltip below
      setTooltipPosition(rect.top < 150 ? 'bottom' : 'top')
    }
  }, [showTooltip])

  const floatingAnimation = prefersReducedMotion
    ? {}
    : {
        y: [0, -4, 0],
        transition: {
          duration: 3,
          repeat: Infinity,
          ease: 'easeInOut' as const,
          delay: index * 0.2,
        },
      }

  const hoverAnimation = prefersReducedMotion
    ? {}
    : {
        scale: 1.15,
        transition: { duration: 0.2, ease: [0.25, 0.1, 0.25, 1] as const },
      }

  const MotionDiv = prefersReducedMotion ? 'div' : motion.div

  return (
    <div
      ref={iconRef}
      className="relative flex items-center justify-center"
      role="listitem"
      onMouseEnter={() => setShowTooltip(true)}
      onMouseLeave={() => setShowTooltip(false)}
      onFocus={() => setShowTooltip(true)}
      onBlur={() => setShowTooltip(false)}
    >
      <MotionDiv
        {...(!prefersReducedMotion && {
          animate: floatingAnimation,
          whileHover: hoverAnimation,
        })}
        className="group relative flex items-center justify-center w-16 h-16 md:w-20 md:h-20 
          rounded-xl bg-[var(--color-card)] border border-[var(--color-border)]
          cursor-pointer transition-[border-color,box-shadow] duration-300
          hover:border-[var(--color-primary)]/60
          focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[var(--color-primary)]"
        style={{
          boxShadow: showTooltip
            ? `0 0 20px ${config.color}40, 0 0 40px ${config.color}20`
            : 'none',
        }}
        tabIndex={0}
        aria-label={config.name}
        role="button"
        aria-describedby={showTooltip ? `tooltip-${tech}` : undefined}
      >
        {/* Neon glow background on hover */}
        <div
          className="absolute inset-0 rounded-xl opacity-0 group-hover:opacity-20 
            transition-opacity duration-300 pointer-events-none"
          style={{
            background: `radial-gradient(circle at 50% 50%, ${config.color} 0%, transparent 70%)`,
          }}
          aria-hidden="true"
        />

        {/* Icon */}
        <div
          className="relative z-10 text-[var(--color-text)] group-hover:text-[var(--color-primary)]
            transition-colors duration-300"
          style={{
            color: showTooltip ? config.color : undefined,
          }}
        >
          {config.icon}
        </div>
      </MotionDiv>

      {/* Tooltip */}
      <AnimatePresence>
        {showTooltip && (
          <motion.div
            id={`tooltip-${tech}`}
            role="tooltip"
            initial={{ opacity: 0, y: tooltipPosition === 'top' ? 8 : -8 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: tooltipPosition === 'top' ? 8 : -8 }}
            transition={{ duration: prefersReducedMotion ? 0 : 0.15 }}
            className={`absolute z-20 px-3 py-1.5 rounded-lg 
              bg-[var(--color-card)] border border-[var(--color-border)]
              text-sm font-medium text-[var(--color-text)] whitespace-nowrap
              shadow-lg pointer-events-none
              ${tooltipPosition === 'top' ? 'bottom-full mb-2' : 'top-full mt-2'}`}
            style={{
              boxShadow: `0 4px 20px ${config.color}30`,
            }}
          >
            {config.name}
            {/* Tooltip arrow */}
            <div
              className={`absolute left-1/2 -translate-x-1/2 w-2 h-2 rotate-45
                bg-[var(--color-card)] border-[var(--color-border)]
                ${tooltipPosition === 'top' 
                  ? 'top-full -mt-1 border-r border-b' 
                  : 'bottom-full -mb-1 border-l border-t'}`}
              aria-hidden="true"
            />
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  )
}

